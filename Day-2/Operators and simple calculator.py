## Operators

'''
+: addition, -: substraction, *: multiplication, / : divide, % : modulo, //: float division, ** : Exponential
'''

#example

a = 2
b = 5

c = a+b
d = a-b
e = a*b
f = a/b
g = a%b
h = a//b
i = a**b

print(c,d,e,f,g,h,i)
#output : 7 -3 10 0.4 2 0 32

